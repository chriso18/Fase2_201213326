package Modelo;

/**
 *
 * @author Christian
 */
public class Conocimiento {
    String nombre;
    String usuario;
    
    public Conocimiento(){
        nombre="";
        usuario="";
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
    
}
